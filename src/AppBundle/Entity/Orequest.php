<?php

namespace AppBundle\Entity;

/**
 * Orequest
 */
class Orequest
{
    /**
     * @var integer
     */
    private $idRequest;

    /**
     * @var integer
     */
    private $company;

    /**
     * @var integer
     */
    private $client;

    /**
     * @var integer
     */
    private $basket;


    /**
     * Get idRequest
     *
     * @return integer
     */
    public function getIdRequest()
    {
        return $this->idRequest;
    }

    /**
     * Set company
     *
     * @param integer $company
     *
     * @return Orequest
     */
    public function setCompany($company)
    {
        $this->company = $company;

        return $this;
    }

    /**
     * Get company
     *
     * @return integer
     */
    public function getCompany()
    {
        return $this->company;
    }

    /**
     * Set client
     *
     * @param integer $client
     *
     * @return Orequest
     */
    public function setClient($client)
    {
        $this->client = $client;

        return $this;
    }

    /**
     * Get client
     *
     * @return integer
     */
    public function getClient()
    {
        return $this->client;
    }

    /**
     * Set basket
     *
     * @param integer $basket
     *
     * @return Orequest
     */
    public function setBasket($basket)
    {
        $this->basket = $basket;

        return $this;
    }

    /**
     * Get basket
     *
     * @return integer
     */
    public function getBasket()
    {
        return $this->basket;
    }
}
